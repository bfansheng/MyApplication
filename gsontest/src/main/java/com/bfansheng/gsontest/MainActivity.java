    private void sendRequestWithHttpURLConnect() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                HttpURLConnection connection = null;
                try {
                    URL url = new URL("http://www.weather.com.cn/adat/sk/101230101.html");
                    connection = (HttpURLConnection) url.openConnection();
                    connection.setRequestMethod("GET");
                    connection.setConnectTimeout(8000);
                    connection.setReadTimeout(8000);
                    InputStream in = connection.getInputStream();
                    BufferedReader reader = new BufferedReader(new InputStreamReader(in));
                    StringBuilder result = new StringBuilder();
                    String line;
                    while ((line = reader.readLine()) != null) {
                        result.append(line);
                    }
                    Log.i("======", result.toString());
                    parseJSONWithGSON(result.toString());
                } catch (Exception e) {
                    e.printStackTrace();
                }finally {
                    if (connection != null) {
                        connection.disconnect();
                    }
                }
            }
        }).start();
    }

    private void parseJSONWithGSON(String jsonData) {
        Gson gson = new Gson();
        try {
            //
            JSONObject jsonObject = new JSONObject(jsonData);
            JSONObject jsonCity = new JSONObject(
                    jsonObject.getString("weatherinfo"));
            Weather weather = gson.fromJson(jsonCity.toString(), Weather.class);
            Log.i("city", weather.getCity());
        }catch (Exception e) {
            e.printStackTrace();
        }
    }

